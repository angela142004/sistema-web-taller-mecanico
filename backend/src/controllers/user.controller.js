// controllers/user.controller.js
import { PrismaClient } from "@prisma/client";
import bcrypt from "bcryptjs";
import jwt from "jsonwebtoken";
import { config } from "../config/env.js";

const prisma = new PrismaClient();

// 游늷 Registrar usuario
export const registerUser = async (req, res) => {
  try {
    const { nombre, correo, contrase침a, rol } = req.body;

    // Validar campos obligatorios
    if (!nombre || !correo || !contrase침a) {
      return res
        .status(400)
        .json({ message: "Todos los campos son obligatorios" });
    }

    // Validar rol
    const rolValido = ["cliente", "mecanico", "admin"];
    const rolFinal = rolValido.includes(rol) ? rol : "cliente";

    // Validar si el usuario ya existe (por nombre o correo)
    const existingUser = await prisma.usuarios.findFirst({
      where: {
        OR: [{ nombre: nombre }, { correo: correo }],
      },
    });

    if (existingUser) {
      return res.status(400).json({ message: "Usuario ya existe" });
    }

    // Hashear contrase침a
    const hashedPassword = await bcrypt.hash(contrase침a, 10);

    // Crear usuario
    const newUser = await prisma.usuarios.create({
      data: {
        nombre,
        correo,
        contrase침a: hashedPassword,
        rol: rolFinal,
      },
    });

    res.status(201).json({
      message: "Usuario registrado correctamente",
      user: {
        id_usuario: newUser.id_usuario,
        nombre: newUser.nombre,
        correo: newUser.correo,
        rol: newUser.rol,
      },
    });
  } catch (error) {
    console.log(error);
    res.status(500).json({
      message: "Error al registrar usuario",
      error: error.message,
    });
  }
};

// 游늷 Login de usuario
export const loginUser = async (req, res) => {
  try {
    const { correo, contrase침a } = req.body;

    // Buscar usuario por correo
    const user = await prisma.usuarios.findUnique({ where: { correo } });
    if (!user) {
      return res.status(404).json({ message: "Usuario no encontrado" });
    }

    // Verificar contrase침a
    const isPasswordValid = await bcrypt.compare(contrase침a, user.contrase침a);
    if (!isPasswordValid) {
      return res.status(401).json({ message: "Contrase침a incorrecta" });
    }

    // Crear token JWT
    const token = jwt.sign(
      { id_usuario: user.id_usuario, correo: user.correo, rol: user.rol },
      config.jwtSecret,
      { expiresIn: "1h" }
    );

    res.json({
      message: "Login exitoso",
      token,
      user: {
        id_usuario: user.id_usuario,
        nombre: user.nombre,
        correo: user.correo,
        rol: user.rol,
      },
    });
  } catch (error) {
    console.log(error);
    res
      .status(500)
      .json({ message: "Error al iniciar sesi칩n", error: error.message });
  }
};

// 游늷 Obtener todos los usuarios
export const getUsers = async (req, res) => {
  try {
    const users = await prisma.usuarios.findMany({
      select: {
        id_usuario: true,
        nombre: true,
        correo: true,
        rol: true,
      },
    });
    res.json(users);
  } catch (error) {
    console.log(error);
    res
      .status(500)
      .json({ message: "Error al obtener usuarios", error: error.message });
  }
};

// 游늷 Obtener un usuario por ID
export const getUserById = async (req, res) => {
  try {
    const { id } = req.params;
    const user = await prisma.usuarios.findUnique({
      where: { id_usuario: Number(id) },
      select: {
        id_usuario: true,
        nombre: true,
        correo: true,
        rol: true,
      },
    });

    if (!user) {
      return res.status(404).json({ message: "Usuario no encontrado" });
    }

    res.json(user);
  } catch (error) {
    console.log(error);
    res
      .status(500)
      .json({ message: "Error al obtener el usuario", error: error.message });
  }
};

// 游늷 Actualizar usuario
export const updateUser = async (req, res) => {
  try {
    const { id } = req.params;
    const { nombre, correo, contrase침a, rol } = req.body;

    const hashedPassword = contrase침a
      ? await bcrypt.hash(contrase침a, 10)
      : undefined;

    const updatedUser = await prisma.usuarios.update({
      where: { id_usuario: Number(id) },
      data: {
        nombre,
        correo,
        rol,
        ...(hashedPassword && { contrase침a: hashedPassword }),
      },
      select: {
        id_usuario: true,
        nombre: true,
        correo: true,
        rol: true,
      },
    });

    res.json(updatedUser);
  } catch (error) {
    console.log(error);
    res.status(500).json({
      message: "Error al actualizar el usuario",
      error: error.message,
    });
  }
};

// 游늷 Eliminar usuario
export const deleteUser = async (req, res) => {
  try {
    const { id } = req.params;

    await prisma.usuarios.delete({
      where: { id_usuario: Number(id) },
    });

    res.json({ message: "Usuario eliminado correctamente" });
  } catch (error) {
    console.log(error);
    res
      .status(500)
      .json({ message: "Error al eliminar el usuario", error: error.message });
  }
};
